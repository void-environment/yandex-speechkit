    """–ó–∞–ø—É—Å–∫ —Å–µ—Ä–≤–µ—Ä–∞"""
    print("üöÄ –ó–∞–ø—É—Å–∫ Speech-to-Text –º–∏–∫—Ä–æ—Å–µ—Ä–≤–∏—Å–∞")
